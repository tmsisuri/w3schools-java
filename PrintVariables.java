public class PrintVariables {
    public static void main(String[] args) {
        String name = "john";
        System.out.println("Hello " + name);

        String firstName = "John";
        String lastName = "Doe";
        String fullName = firstName + lastName;
        System.out.println(fullName);

        int x = 4;
        int y = 8;
        System.out.println(x + y);
    }
}

public class DeclareMultipleVariables {
    public static void main(String[] args) {
        //declare many variables
        int x = 5, y = 6, z = 50;
        System.out.println(x + y + z);

        //one value to multiple variables
        int a, b, c;
        a = b = c = 50;
        System.out.println(a + b + c);
    }
}
